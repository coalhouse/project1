    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        if (costcentreResult != null) {
            int startDay = new Integer(daySelect.getSelectedItem().toString());
            int startMonth = new Integer(monthSelect.getSelectedIndex());
            int startYear = new Integer(yearSelect.getSelectedItem().toString());
            GregorianCalendar startDate = new GregorianCalendar(startYear, startMonth, startDay);


            int endDay = new Integer(daySelect1.getSelectedItem().toString());
            int endMonth = new Integer(monthSelect1.getSelectedIndex());
            int endYear = new Integer(yearSelect1.getSelectedItem().toString());
            GregorianCalendar endDate = new GregorianCalendar(endYear, endMonth, endDay);


            boolean type;
            if (limitType.getSelectedIndex() == 0) {
                type = true;
            } else {
                type = false;
            }
            if (startDate.before(endDate)) {
                costcentreResult.setName(costcentreNameInput.getText());
                costcentreResult.setStartDate(startDate);
                costcentreResult.setEndDate(endDate);
                costcentreResult.setHardLimit(type);
                costcentreResult.setLimitAmount(new Double(amountInput.getText()));
                this.findCostCentre();
                setStatus("Cost Centre successfully updated!");
            } else {
                setStatus("Please check - Start date is after end date");
            }
        } else {
            setStatus("Please enter an ID to search for");
        }
        
    }//GEN-LAST:event_updateActionPerformed

