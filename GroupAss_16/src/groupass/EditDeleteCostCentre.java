    public void findCostCentre() {
        try {
            if (!"".equals(costcentreIDinput.getText())) {
                costcentreResult = CostCentre.getCostCentreByID(new Integer(costcentreIDinput.getText()));
                Integer ccID = new Integer(costcentreIDinput.getText());
                CostCentre cc = CostCentre.getCostCentreByID(ccID);
                
                if (cc != null) {
                    costcentreID.setText("" + cc.getCostCentreId());
                    costcentreNameInput.setText(cc.getName());

                    daySelect.setSelectedIndex(cc.getStartDate().get(Calendar.DAY_OF_MONTH));
                    monthSelect.setSelectedIndex(cc.getStartDate().get(Calendar.MONTH) + 1);
                    yearSelect.setSelectedIndex(cc.getStartDate().get(Calendar.YEAR) - 2005 + 1);

                    daySelect1.setSelectedIndex(cc.getEndDate().get(Calendar.DAY_OF_MONTH));
                    monthSelect1.setSelectedIndex(cc.getEndDate().get(Calendar.MONTH) + 1);
                    yearSelect1.setSelectedIndex(cc.getEndDate().get(Calendar.YEAR) - 2005 + 1);
                if (cc.isHardLimit()) {
                    limitType.setSelectedIndex(0);
                } else {
                    limitType.setSelectedIndex(1);
                }
                amountInput.setText("" + cc.getLimitAmount());
                setStatus("Cost Centre " + costcentreID.getText() + " found! - You can edit or delete it");
                }
                else {
                   setStatus("No Cost Centre found with this ID");
                }
            } else {
                setStatus("Please enter an ID to search for");
            }
        } catch (java.lang.NumberFormatException nfe) {
            setStatus("Invalid Cost Centre ID - Search again");
        }
    }

