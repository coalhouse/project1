    public void setSysDate() {
        GregorianCalendar sysDate = new GregorianCalendar();
        daySelect.setSelectedIndex(sysDate.get(Calendar.DAY_OF_MONTH));
        monthSelect.setSelectedIndex(sysDate.get(Calendar.MONTH) + 1);
        yearSelect.setSelectedIndex(sysDate.get(Calendar.YEAR) - 2004);
    }
