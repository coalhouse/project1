    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        try {
            if (expenseIDinput.getText() != null && payeeInput.getText() != null && daySelect.getSelectedIndex() != 0 && monthSelect.getSelectedIndex() != 0 && yearSelect.getSelectedIndex() != 0) {
                Integer expenseID = new Integer(expenseIDinput.getText());
                int day = new Integer(daySelect.getSelectedItem().toString());
                int month = new Integer(monthSelect.getSelectedIndex());
                int year = new Integer(yearSelect.getSelectedItem().toString());
                GregorianCalendar date = new GregorianCalendar(year, month, day);

                String payee = payeeInput.getText();
                String desc = descInput.getText();
                Expense newExpense = new Expense(expenseID, date, payee, desc);
                setStatus("New expense created");
                newExpense.addExpense();

                AddExpenseToCC logExpense = new AddExpenseToCC();
                logExpense.setReturnExpenseScreen(this);
                this.setVisible(false);
                logExpense.setVisible(true);
                logExpense.setStatus("Expense successfully created - you can now log it against an existing cost centre");
            }
            else {
                setStatus("Please fill in all the required fields");
            }
        } catch (java.lang.NumberFormatException nfe) {
            setStatus("Invalid expense ID");
        }

    }//GEN-LAST:event_nextActionPerformed

